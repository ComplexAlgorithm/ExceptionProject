
public class NegativeSideException extends Exception {
	//instance variables
	//none
	
	//default constructor
	public NegativeSideException() {
		super(); //calls the constructor of the parent class (Exception)
	}
	
	//overloaded constructor 
	public NegativeSideException(String message) {
		super(message); //calls the overloaded constructor of the parent class (Exception)
	}
	
	//non-static methods
	//none 
}
